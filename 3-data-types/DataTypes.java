
public class DataTypes {
  public static void main(String[] args) {

    // DataTypes
    byte a = 5;
    short b = 3;
    int c = 4;
    double d = 3.2;
    long e = 957934593;
    boolean f = true;
    char g = 'V';

    // Print statements
    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
    System.out.println(f);
    System.out.println(g);
  }
}
